
public class Employee {

    String day;
    double date;
    double time;

	public Employee(String day, double date, double time){
        this.day = day;
        this.date = date;
        this.time = time;
    }
    public String getDay(){
        return day;
    }
    public double getDate(){
        return date;
    }
    public double getTime(){
        return time;
    }
}
